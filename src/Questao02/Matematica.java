package Questao02;

public class Matematica {
    double n1, n2, soma, subtracao, divisao, multiplicacao;

    public double somar() {
        soma = n1 + n2;
        return soma;
    }
    public double subtrair() {
        subtracao = n1 - n2;
        return subtracao;
    }
    public double dividir() {
        divisao = n1 / n2;
        return divisao;
    }
    public double multiplicar() {
        multiplicacao = n1 * n2;
        return multiplicacao;
    }
}
